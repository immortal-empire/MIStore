package com.neu.prodetail.model.bean;

public class CommodityOneType {
	private Integer comtyId;
	private String comtyName;
	private String state;
	
	public Integer getComtyId() {
		return comtyId;
	}
	public void setComtyId(Integer comtyId) {
		this.comtyId = comtyId;
	}
	public String getComtyName() {
		return comtyName;
	}
	public void setComtyName(String comtyName) {
		this.comtyName = comtyName;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
}
